#include<stdio.h>

int main()
{   
        int a[10] = {0};//前面数组的10其实不用写也是可以的，如果你给它赋了值的话
        int i;

        for (i = 0 ; i < 10 ; i++)
        {
                printf("%d\n",a[i]);
        }

        return 0;
}

//可以只给一部分元素赋值，为赋值的自动初始化为0
